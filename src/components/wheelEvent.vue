const scrollable = document.querySelector('.scrollable');
const scrollStep = 50;  // Number of pixels to scroll per step

scrollable.addEventListener('wheel', function(e) {
    const scrollLeft = this.scrollLeft;
    const scrollWidth = this.scrollWidth - this.clientWidth;
    const delta = Math.sign(e.deltaY) * scrollStep;

    const newScrollLeft = Math.max(0, Math.min(scrollLeft + delta, scrollWidth));
    
    // Allow default scroll behavior if at the beginning or end of content
    if (newScrollLeft === 0 || newScrollLeft === scrollWidth) {
        e.stopPropagation();  // Stop propagation to parent elements
        return;
    }

    e.preventDefault();  // Prevent default scroll behavior
    this.scrollLeft = newScrollLeft;
}, { passive: false });



